package org.jorm.actions;

import org.jorm.annotations.Model;

@Model
public class TestClass {
    int id;
    String name;

    public TestClass(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
